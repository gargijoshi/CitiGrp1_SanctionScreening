package com.citi.sanction_transaction.storage;

import java.io.IOException;
import java.io.InputStream;
import java.net.MalformedURLException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.nio.file.StandardCopyOption;
import java.text.ParseException;
import java.util.List;
import java.util.stream.Stream;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.core.io.Resource;
import org.springframework.core.io.UrlResource;
import org.springframework.stereotype.Service;
import org.springframework.util.FileSystemUtils;
import org.springframework.util.StringUtils;
import org.springframework.web.multipart.MultipartFile;

import com.citi.sanction_transaction.helper.CSVHelper;
import com.citi.sanction_transaction.model.FileDB;
import com.citi.sanction_transaction.repository.FileDBRepository;
import com.citi.sanction_transaction.storage.StorageService;

//import com.example.uploadingfiles.TransactionController;

@Service
public class FileSystemStorageService implements StorageService {

	private final Path rootLocation;

	@Autowired
	private FileDBRepository fileDBRepo;

	@Autowired
	CSVService csv;

	public FileSystemStorageService(StorageProperties properties) {
		this.rootLocation = Paths.get(properties.getLocation());
	}

	@Override
	public String store(MultipartFile file) {
		try {
			if (file.isEmpty()) {
//				throw new StorageException("Failed to store empty file.");
				System.out.println("Blank!");
				return "redirect:/uploadBlank";
			}
			
			String fileName = StringUtils.cleanPath(file.getOriginalFilename());
			FileDB fileDB = new FileDB(fileName, file.getContentType());
			
			if(!new CSVHelper().hasCSVFormat(file)) {
//				throw new StorageException("Cannot store file");
//				System.exit(0);
				System.out.println("Incorrect Format");
				return "redirect:/uploadFail";
			}			
			fileDBRepo.save(fileDB);			
			
			Path destinationFile = this.rootLocation.resolve(
					Paths.get(file.getOriginalFilename()))
					.normalize().toAbsolutePath();
			if (!destinationFile.getParent().equals(this.rootLocation.toAbsolutePath())) {
				// This is a security check
				throw new StorageException(
						"Cannot store file outside current directory.");
			}
			try (InputStream inputStream = file.getInputStream()) {
				Files.copy(inputStream, destinationFile,
					StandardCopyOption.REPLACE_EXISTING);
			}
		}
		catch (IOException e) {
			throw new StorageException("Failed to store file.", e);
		}
		try {
			System.out.println("Name: " + file.getOriginalFilename());
			csv.save(file, file.getOriginalFilename());
		}catch(Exception e) {
			System.out.print(e.getMessage()	);
		}
		return "Success";
	}

	@Override
	public Stream<Path> loadAll() {
		try {
			return Files.walk(this.rootLocation, 1)
				.filter(path -> !path.equals(this.rootLocation))
				.map(this.rootLocation::relativize);
		}
		catch (IOException e) {
			throw new StorageException("Failed to read stored files", e);
		}

	}

	@Override
	public Path load(String filename) {
		return rootLocation.resolve(filename);
	}

	@Override
	public Resource loadAsResource(String filename) {
		try {
			Path file = load(filename);
			Resource resource = new UrlResource(file.toUri());
			if (resource.exists() || resource.isReadable()) {
				return resource;
			}
			else {
				throw new StorageFileNotFoundException(
						"Could not read file: " + filename);

			}
		}
		catch (MalformedURLException e) {
			throw new StorageFileNotFoundException("Could not read file: " + filename, e);
		}
	}

	@Override
	public void init() {
		try {
			Files.createDirectories(rootLocation);
		}
		catch (IOException e) {
			throw new StorageException("Could not initialize storage", e);
		}
	}
}
